ctx.fillStyle = 'red';         // цвет заливки
ctx.fillRect(10, 10, 100, 50); // рисуем прямоугольник

ctx.strokeStyle = 'blue';      // цвет обводки
ctx.lineWidth = 3;             // толщина линии
ctx.strokeRect(50, 50, 80, 40); // прямоугольник с обводкой

ctx.beginPath();               // начать новый путь
ctx.moveTo(100, 100);          // переместить "перо"
ctx.lineTo(200, 150);          // линия до точки (200, 150)
ctx.stroke();                  // нарисовать линию